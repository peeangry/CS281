package Model;

public class Student {
	private static String stuid;
	private static int stupoint;
	private static char stugrade;
	
	public static String getStuid() {
		return stuid;
	}
	public static void setStuid(String stuid) {
		Student.stuid = stuid;
	}
	public static int getStupoint() {
		return stupoint;
	}
	public static void setStupoint(int stupoint) {
		Student.stupoint = stupoint;
	}
	public static char getStugrade() {
		return stugrade;
	}
	public static void setStugrade(char stugrade) {
		Student.stugrade = stugrade;
	}

	
	
}
